func TestAddVersionHeader_PreservesComments(t *testing.T) {
	content := []byte("# comment\npipeline: foo\n")
	result := addVersionHeader(content, "0.3")
	lines := strings.Split(string(result), "\n")
	if lines[0] != "# comment" {
		t.Errorf("first line = %q, want comment", lines[0])
	}
	if lines[1] != `version: "0.3"` {
		t.Errorf("second line = %q, want version", lines[1])
	}
}

func TestWriteBackup(t *testing.T) {
	dir := t.TempDir()
	configPath := filepath.Join(dir, "pipeline.yaml")
	content := []byte("pipeline: test\n")

	if err := os.WriteFile(configPath, content, 0644); err != nil {
		t.Fatal(err)
	}

	backupPath, err := WriteBackup(configPath)
	if err != nil {
		t.Fatalf("WriteBackup error: %v", err)
	}

	if !strings.HasSuffix(backupPath, ".bak") {
		t.Errorf("backup path %q should end with .bak", backupPath)
	}

	backupContent, err := os.ReadFile(backupPath)
	if err != nil {
		t.Fatalf("reading backup: %v", err)
	}

	if string(backupContent) != string(content) {
		t.Errorf("backup content mismatch")
	}
}
